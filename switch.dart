void main() {
  int dayOfWeek = 2;

  switch (dayOfWeek) {
    case 1:
      print('Day is Monday');
      break;
    case 2:
      print('Day is Tuesday');
      break;
    case 3:
      print('Day is Wednesday');
      break;
    case 4:
      print('Day is Thursday');
      break;
    case 5:
      print('Day is Friday');
      break;
    case 6:
      print('Day is Saturday');
      break;
    case 7:
      print('Day is Sunday');
      break;
    default:
      print('Invalid Day');
  }
}
